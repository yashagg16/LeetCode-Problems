class Solution{
  public:
    TNode* makeBST(LNode*& head, int n){
        if(n <= 0) return NULL;
        
        TNode* left = makeBST(head, n/2);
        TNode* x = new TNode((head)->data);
        x->left = left;
        head = (head)->next;
        // cout << "new data " << local_head->data << endl;
        x->right = makeBST(head, n-n/2-1);
    
        return x;
    }
    TNode* sortedListToBST(LNode *head) {
        //code here
        int n = 0;
        LNode *tmp = head;
        
        while(tmp) {
            tmp = tmp->next;
            n++;
        }
        return makeBST(head, n);
        
    }
};

